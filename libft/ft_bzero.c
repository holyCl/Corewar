#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ss;

	i = 0;
	ss = (unsigned char *)s;
	if (n != 0)
	{
		while (i < n)
		{
			ss[i] = 0;
			i++;
		}
	}
}
