#include "libft.h"

unsigned char	ft_reverse_bits(unsigned char octet)
{
	return (((octet << 7) & 128) | ((octet << 5) & 64) |
	((octet << 3) & 32) | ((octet << 1) & 16) | ((octet >> 1) & 8) |
	((octet >> 3) & 4) | ((octet >> 5) & 2) | ((octet >> 7) & 1));
}
