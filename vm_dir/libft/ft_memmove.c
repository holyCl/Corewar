#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;

	s = (char*)src;
	d = (char*)dst;
	if (s < d)
	{
		s = s + len - 1;
		d = d + len - 1;
		while (len > 0)
		{
			*d-- = *s--;
			len--;
		}
	}
	else
		return (ft_memcpy(d, s, len));
	return (dst);
}
